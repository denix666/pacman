pub struct Map {
    pub lvl_num: i32,
}

impl Map {
    pub async fn new(lvl_num: i32) -> Self {
        Self {
            lvl_num,
        }
    }

    pub fn draw(&mut self) {
        let lvl = match self.lvl_num {
            1  => LVL1,
            2  => LVL2,
            3  => LVL3,
            _ => {
                panic!("no such level");
            }
        };

        let mut x: f32 = 0.0;
        let mut y: f32 = 0.0;

        let color:Color = match self.lvl_num {
            1 => BLUE,
            2 => SKYBLUE,
            3 => LIGHTGRAY,
            _ => {
                panic!("no such level");
            }
        };

        for i in 0..lvl.len() {
            y += 50.0;
            for (_, &value) in lvl[i].iter().enumerate() {
                x += 50.0;
                if value == 1 {
                    draw_rectangle(x - 50.0, y - 50.0, 50.0, 50.0, color)
                }
            }
            x = 0.0;
        }
    }
}